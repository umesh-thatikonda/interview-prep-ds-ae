CREATE TABLE IF NOT EXISTS stations (
    station_id     TEXT PRIMARY KEY,
    station_name   TEXT NOT NULL,
    region         TEXT,
    capacity       INTEGER,
    lat            REAL,
    lng            REAL
);

CREATE TABLE IF NOT EXISTS trips (
    ride_id            TEXT PRIMARY KEY,
    rideable_type      TEXT,             -- classic_bike | electric_bike | docked_bike
    started_at         DATETIME,
    ended_at           DATETIME,
    start_station_id   TEXT,
    end_station_id     TEXT,
    start_lat          REAL,
    start_lng          REAL,
    end_lat            REAL,
    end_lng            REAL,
    member_casual      TEXT              -- member | casual
);

CREATE TABLE IF NOT EXISTS weather (
    date           DATE PRIMARY KEY,
    temp_c         REAL,
    feels_like_c   REAL,
    humidity_pct   INTEGER,
    wind_kmh       REAL,
    condition      TEXT                  -- Clear | Cloudy | Rain | Snow
);

